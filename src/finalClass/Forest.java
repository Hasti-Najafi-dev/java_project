package finalClass;

public class Forest extends Land{
	private String hectare ;
	private String type ;
	
	
	public void setNumber(String h) {
		hectare = h ;
	}
	public String getNumber() {
		return hectare ;
	}
	
	
	public void setType(String t) {
		type = t ;
	}
	public String getType() {
		return type ;
	}

}
